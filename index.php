<p>
Strawberry is a music player and music collection organizer. It is a fork of Clementine released in 2018 aimed at music collectors and audiophiles. It's written in C++ using the Qt toolkit.<br />
</p>
<div class="round-box">
<ul>
<li>Play and organize music</li>
<li>Supports WAV, FLAC, WavPack, Ogg Vorbis, Speex, MPC, TrueAudio, AIFF, MP4, MP3, ASF and Monkey's Audio</li>
<li>Audio CD playback</li>
<li>Native desktop notifications</li>
<li>Playlists in multiple formats</li>
<li>Advanced audio output and device configuration for bit-perfect playback on Linux</li>
<li>Edit tags on music files</li>
<li>Fetch tags from MusicBrainz</li>
<li>Album cover art from <a href="https://www.last.fm/">Last.fm</a>, <a href="https://musicbrainz.org/">Musicbrainz</a>, <a href="https://www.discogs.com/">Discogs</a>, <a href="https://www.musixmatch.com/">Musixmatch</a>, <a href="https://www.deezer.com/">Deezer</a>, <a href="https://www.tidal.com/">Tidal</a>, <a href="https://www.qobuz.com/">Qobuz</a> and <a href="https://www.spotify.com/">Spotify</a></li>
<li>Song lyrics from <a href="https://audd.io/">AudD</a>, <a href="https://genius.com/">Genius</a>, <a href="https://www.musixmatch.com/">Musixmatch</a>, <a href="http://www.chartlyrics.com/">ChartLyrics</a>, <a href="https://lyrics.ovh/">lyrics.ovh</a> and <a href="https://www.lololyrics.com/">lololyrics.com</a></li>
<li>Support for multiple backends</li>
<li>Audio analyzer</li>
<li>Audio equalizer</li>
<li>Transfer music to iPod, MTP or mass-storage USB player</li>
<li>Scrobbler with support for <a href="https://www.last.fm/">Last.fm</a>, <a href="https://libre.fm/">Libre.fm</a> and <a href="https://listenbrainz.org/">ListenBrainz</a></li>
<li>Streaming support for Subsonic and Tidal</li>
</ul>
</div>

